    public static ArrayList<Aspirante> listaAspirantes = new ArrayList<>();
       
    private void saveButtonClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveButtonClicked
        
        String nameV = (String)name.getText();
        String lastNameV = (String)lastName.getText();
        String emailV = (String)email.getText();
        String phoneNumberV = (String)phoneNumber.getText();
        String ageV = (String)age.getText();
        String idNumberV = (String)idNumber.getText();
        String professionV = (String)profession.getSelectedItem();
        
        String genderV;
        
        if(male.isSelected()){
            genderV = "Hombre";
        }else if(female.isSelected()){
            genderV = "Mujer";
        }else{
            genderV = "No Binario";
        }
        
        String educationPlaceV = (String)educationPlace.getText();
        String degreeV = (String)degree.getText();
        String startDateDegreeV = (String)startDateDegree.getText();
        String endDateDegreeV = (String)endDateDegree.getText();
        String lastCompanyV = (String)lastCompany.getText();

        String startLastJobV = (String)startLastJob.getText();
        String endLastJobV = (String)endLastJob.getText();
        String yearsOfExperienceV = (String)yearsOfExperience.getText();
        String positionLastJobV = (String)positionLastJob.getText();
        
        Aspirante aspirante = new Aspirante(nameV, lastNameV, emailV, phoneNumberV, ageV, idNumberV,
        professionV, genderV, educationPlaceV, degreeV, startDateDegreeV, endDateDegreeV,
        lastCompanyV, startLastJobV, endLastJobV, yearsOfExperienceV, positionLastJobV);
        
        listaAspirantes.add(aspirante);
        
        this.setVisible(false);
        MainMenuApp v = new MainMenuApp();
        v.setVisible(true);
  
    }//GEN-LAST:event_saveButtonClicked

